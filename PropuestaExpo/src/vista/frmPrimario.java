    private void lblAplicarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblAplicarMouseClicked
        if (rTema1.isSelected() == true && rTema2.isSelected() == false && rTema3.isSelected() == false && rTema4.isSelected() == false && rTema5.isSelected() == false) {
            cambioDeColor(35,55,77,1);
            cambioDeColor(16,137,255,2);
            cambioDeColor(229,229,229,3);
        }
        else if (rTema1.isSelected() == false && rTema2.isSelected() == true && rTema3.isSelected() == false && rTema4.isSelected() == false && rTema5.isSelected() == false) {
            cambioDeColor(66,0,0,1);
            cambioDeColor(212,215,221,2);
            cambioDeColor(246,246,246,3);
        }
        else if (rTema1.isSelected() == false && rTema2.isSelected() == false && rTema3.isSelected() == true && rTema4.isSelected() == false && rTema5.isSelected() == false) {
            cambioDeColor(34,40,49,1);
            cambioDeColor(50,224,196,2);
            cambioDeColor(57,62,70,3);
        }
        else if (rTema1.isSelected() == false && rTema2.isSelected() == false && rTema3.isSelected() == false && rTema4.isSelected() == true && rTema5.isSelected() == false) {
            cambioDeColor(207,117,0,1);
            cambioDeColor(240,165,0,2);
            cambioDeColor(264,244,244,3);
        }
        else if (rTema1.isSelected() == false && rTema2.isSelected() == false && rTema3.isSelected() == false && rTema4.isSelected() == false && rTema5.isSelected() == true) {
            cambioDeColor(255,115,21,1);
            cambioDeColor(58,53,53,2);
            cambioDeColor(244,244,244,3);
        }
        else if (rPersonal.isSelected() == true){
            colorTop = pnlSeleccionDeColorTop.getBackground();
            colorleft = pnlSeleccionDeColorLeft.getBackground();
            colorBack = pnlSeleccionDeColorBack.getBackground();
            
            pnlTop.setBackground(colorTop);
            pnlMenu.setBackground(colorleft);
            pnlContenedor.setBackground(colorBack);
        }
    }//GEN-LAST:event_lblAplicarMouseClicked

